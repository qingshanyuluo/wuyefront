<template>
  <div class="div">
    <el-form ref="loginForm" :model="form" :rules="rules" label-width="80px" class="login-box">
      <h3 class="login-title">欢迎登录</h3>
      <el-form-item label="账号" prop="username" class="username">
        <el-input type="text" placeholder="请输入账号" v-model="form.username"/>
      </el-form-item>
      <el-form-item label="密码" prop="password" class="password">
        <el-input type="password" placeholder="请输入密码" v-model="form.password"/>
      </el-form-item>
      <el-form-item class="submit">
        <el-button type="primary" v-on:click="onSubmit('loginForm')">登录</el-button>
        <el-button type="primary" plain v-on:click="register()">注册</el-button>
      </el-form-item>
    </el-form>

    <el-dialog
      title="温馨提示"
      :visible.sync="dialogVisible"
      width="30%"
      :before-close="handleClose">
      <span>请输入账号和密码</span>
      <span slot="footer" class="dialog-footer">
        <el-button type="primary" @click="dialogVisible = false">确 定</el-button>
      </span>
    </el-dialog>
  </div>
</template>

<script>
export default {
  name: 'ULogin',
  data () {
    return {
      form: {
        username: '',
        password: ''
      },

      // 表单验证，需要在 el-form-item 元素中增加 prop 属性
      rules: {
        username: [
          { required: true, message: '账号不可为空', trigger: 'blur' }
        ],
        password: [
          { required: true, message: '密码不可为空', trigger: 'blur' }
        ]
      },

      // 对话框显示和隐藏
      dialogVisible: false
    }
  },
  methods: {
    onSubmit (formName) {
      // 为表单绑定验证功能
      this.$refs[formName].validate((valid) => {
        if (valid) {
          var _this = this
          let param = new URLSearchParams()
          param.append('name', this.form.username)
          param.append('pwd', this.form.password)
          this.$axios.post('login', param)
            .then(function (response) {
              console.info(response.data)
              if (response.data.type === 0) {
                _this.$router.push({ name: 'Main', params: { user: response.data } })
              } else {
                _this.$alert('请使用管理员平台登录', '提示', {})
              }
            })
            .catch(function (error) {
              _this.$alert('账号或密码输入错误', '提示', {})
              console.info('dds' + error)
              this.dialogVisible = true
              return false
            })
        } else {
          _this.$alert('账号或密码太短', '提示', {})
          this.dialogVisible = true
          return false
        }
      })
    },
    register () {
      this.$router.push('/register')
    }
  }
}
</script>

